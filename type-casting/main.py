# type casting :- it is the process of converting one data type to another data type.

#  int() , flat(), hex(), oct(), str() etc. are the built-in functions for type casting.
a = int('1')
# a = 1
b = int('2')
# b = 2
print(a + b) # addition

# Implicit type casting

c = 1.5
d = 8

print(c + d) # addition type float